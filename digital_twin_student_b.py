# Solar Panel Model (The I-V and P-V Curves)

import numpy as np
import matplotlib.pyplot as plt
from scipy.optimize import fsolve

# ── Panel Parameters (typical 50W panel) ──────────────────────────
Isc_ref  = 3.0      # Short-circuit current at STC [A]
Voc_ref  = 21.6     # Open-circuit voltage at STC [V]
Imp      = 2.8      # Current at max power [A]
Vmp      = 17.8     # Voltage at max power [V]
n        = 1.3      # Ideality factor
Rs       = 0.5      # Series resistance [Ω]
Rsh      = 300.0    # Shunt resistance [Ω]
T_ref    = 298.15   # Reference temperature [K] = 25°C
q        = 1.602e-19
k        = 1.381e-23

def Vt(T):
    return n * k * T / q   # Thermal voltage

def Iph(G, T, G_ref=1000):
    return Isc_ref * (G / G_ref) * (1 + 0.0045*(T - T_ref))

def I0(T):
    return Isc_ref / (np.exp(Voc_ref / Vt(T)) - 1)

def pv_current(V, G=1000, T=298.15):
    """Solve for I given V using the single diode model."""
    def equation(I):
        return (I - Iph(G, T)
                + I0(T)*(np.exp((V + I*Rs)/Vt(T)) - 1)
                + (V + I*Rs)/Rsh)
    I_guess = Iph(G, T) * 0.9
    I_sol, _, flag, _ = fsolve(equation, I_guess, full_output=True)
    return max(I_sol[0], 0)

# ── Generate I-V and P-V curves for different irradiance levels ────
V_range = np.linspace(0, Voc_ref, 200)
irradiance_levels = [1000, 700, 400, 200]  # W/m²
colors = ['#f59e0b', '#f97316', '#6366f1', '#64748b']

fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))
fig.suptitle('Solar Panel Model — Single Diode Model\n(Student B: Digital Twin)',
             fontsize=13, fontweight='bold')

for G, color in zip(irradiance_levels, colors):
    I_vals = np.array([pv_current(v, G=G) for v in V_range])
    P_vals = V_range * I_vals
    label = f'G = {G} W/m²'
    ax1.plot(V_range, I_vals, color=color, lw=2, label=label)
    ax2.plot(V_range, P_vals, color=color, lw=2, label=label)
    # Mark MPP
    mpp_idx = np.argmax(P_vals)
    ax2.plot(V_range[mpp_idx], P_vals[mpp_idx], 'k^', ms=7)

ax1.set(xlabel='Voltage (V)', ylabel='Current (A)', title='I-V Characteristic Curves')
ax1.legend(); ax1.grid(True, alpha=0.3)
ax2.set(xlabel='Voltage (V)', ylabel='Power (W)', title='P-V Curves — ▲ = Maximum Power Point')
ax2.legend(); ax2.grid(True, alpha=0.3)

