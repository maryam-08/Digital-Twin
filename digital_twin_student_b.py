# Simulate solar irradiance: starts clear, cloud passes at t=1200s, recovers
def irradiance_profile(t):
    base = 900
    cloud1 = -600 * np.exp(-((t - 1200)**2) / (2*150**2))  # cloud at 20 min
    cloud2 = -400 * np.exp(-((t - 2500)**2) / (2*200**2))  # cloud at 41 min
    noise  = np.random.normal(0, 20, len(t))
    return np.clip(base + cloud1 + cloud2 + noise, 50, 1000)

G_profile = irradiance_profile(time_seconds)

# Calculate panel output at each second
V_op = 17.0  # Operating voltage (assume MPPT)
I_panel = np.array([pv_current(V_op, G=g) for g in G_profile])
P_solar  = V_op * I_panel

# Battery and load
P_load    = 25.0          # Constant 25W load
C_bat_Ah  = 10.0          # 10 Ah battery
V_bat     = 12.0          # 12V nominal
C_bat_Wh  = C_bat_Ah * V_bat  # 120 Wh

SOC = np.zeros(len(time_seconds))
SOC[0] = 90.0  # Start at 90%
I_battery = np.zeros(len(time_seconds))

for t in range(1, len(time_seconds)):
    P_net = P_solar[t] - P_load           # Positive = charging
    I_battery[t] = P_net / V_bat          # + = charging, - = discharging
    delta_SOC = (I_battery[t] / C_bat_Ah) * (1/3600) * 100  # per second
    SOC[t] = np.clip(SOC[t-1] + delta_SOC, 0, 100)

# Add measurement noise (simulating real sensor imperfection)
V_measured = V_bat + np.random.normal(0, 0.05, len(time_seconds))
I_measured = I_battery + np.random.normal(0, 0.02, len(time_seconds))

# Package as DataFrame (mimics what ESP32 would send via MQTT)
