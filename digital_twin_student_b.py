#This is the heart of the work. The twin receives data and stays synchronized

def digital_twin_sync(df, C_bat_Ah=10.0, V_bat=12.0, sync_window=60):
    """
    Digital Twin with state synchronization.
    Every `sync_window` seconds, the model re-anchors to real sensor data.
    """
    n = len(df)
    SOC_twin   = np.zeros(n)
    SOC_twin[0] = df['SOC_true'].iloc[0]  # Initialize from real data
    sync_error  = np.zeros(n)
    prediction_horizon = np.zeros(n)  # Minutes until SOC hits 20%

    for t in range(1, n):
        row = df.iloc[t]

        # ── Twin's own physics calculation ─────────────────────────
        P_net   = row['P_solar'] - row['P_load']
        I_bat   = P_net / V_bat
        delta   = (I_bat / C_bat_Ah) * (1/3600) * 100
        SOC_twin[t] = np.clip(SOC_twin[t-1] + delta, 0, 100)

        # ── State Synchronization (every sync_window seconds) ──────
        # In real system: this uses V_measured + I_measured from ESP32
        # Here we use SOC_true as the "physical system" reference
        if t % sync_window == 0:
            # Re-anchor model to physical reality
            SOC_twin[t] = row['SOC_true'] * 0.98   # 98% trust in sensor

        # ── Synchronization Error ──────────────────────────────────
        if row['SOC_true'] > 0:
            sync_error[t] = abs(SOC_twin[t] - row['SOC_true']) / row['SOC_true'] * 100

        # ── Prediction: minutes until SOC = 20% ───────────────────
        discharge_rate = -delta * 60  # % per minute (if negative = discharging)
        if discharge_rate > 0 and SOC_twin[t] > 20:
            prediction_horizon[t] = (SOC_twin[t] - 20) / discharge_rate
        else:
            prediction_horizon[t] = 999  # Charging, no concern

    return SOC_twin, sync_error, prediction_horizon

SOC_twin, sync_error, pred_horizon = digital_twin_sync(df)
